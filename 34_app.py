# Tupla

# Crear tupla con diversos tipos de elementos
tupla = (4, 20, "Luis", 15.85, ["manzana", "fresa", "naranjas"], 10)

# Imprimir elementos de tupla
print(tupla)

# Recorrer por los elementos de la tupla de izquierda a derecha
print(tupla[4])

# Recorrer por los elementos de la tupla de derecha a izquierda
print(tupla[-2])